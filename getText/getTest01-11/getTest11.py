    def group_item(self, links, group, num, links_size):
        if links_size <= 0:
            return;
        i = 0
        time.sleep(15)
        for link in links:
            i += 1
            self.get_and_save(link, group, num, i)

    # 按组获取
    def section_load(self, links, group, links_size):
        if links_size <= 0:
            return;
        threads = []
        task_list = []
        task_size = math.ceil(float(links_size) / self.b_loops)
        nloops = range(self.b_loops)
        logger.info('\t\t\t第 %s 组数据，共 %s 本小说，共分为 %s 个线程,每个线程 %s 本小说。 \n' % (group, links_size, self.b_loops, task_size))
        for i in nloops:
            try:
                logger.info("\t\t\t\t\t第 %s 个 ：%s" % (i + 1, str(links[i * task_size:(i + 1) * task_size])))
                task_list.append(links[i * task_size:(i + 1) * task_size])
            except:
                logger.info("\t\t\t\t\t第 %s 个 ：%s" % (i + 1, str(links[i * task_size:])))
                task_list.append(links[i * task_size:])
        print('\n')
        for i in nloops:
            links_size = len(task_list[i])
            if links_size <= 0:
                continue
            t = threading.Thread(target=self.group_item, args=(task_list[i], group, (i + 1), links_size))
            threads.append(t)
        for i in nloops:
            links_size = len(task_list[i])
            if links_size <= 0:
                continue
            logger.info('\t开启 第 %s 组数据，第 %s 个线程，共 %s 条链接，开始抓取...' % (group, i + 1, links_size))
            threads[i].start()
        print('\n')
        for i in nloops:
            if len(task_list[i]) <= 0:
                continue
            threads[i].join()

