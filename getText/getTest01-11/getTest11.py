    def get_catalog(self, catalogData, bookId, bookName):
        links = []
        for i in catalogData:
            for j in i['cs']:
                url = self.b_host + '/chapter/' + bookId + '/' + j['id']
                links.append((j['id'], j['cN'], bookId, bookName, j['cnt'], url, j['uuid'], j['fS']))
        return links

