    def setLinks(self):
        self.errorSize += 1
        self.b_links = self.b_error_urls.copy()
        logger.info(
            '\n\n\n\n\t\t\t抓取存在失败链接 %s 条， 现在 【 第 %s 次  】 重新抓取失败链接' % (len(self.b_error_urls), self.errorSize))
        logger.debug(str(self.b_error_urls))
        self.b_error_urls = []
        self.contents_load()
        self.isOK()

    def isOK(self):
        if self.errorSize >= self.maxErrorSize:
            return;
        if len(self.b_error_urls) > 0:
            self.setLinks()


